function 	is_installed {
	printf "Testing $1 ...";
	RESULT=`whereis $1`
	TOTAL=$((TOTAL+1))
	for ((x = `echo $1 | wc -c`; x < 50; x++)) do printf %s ' '; done;
	if test "$RESULT" = "$1:";
	then
		printf "\e[1;31mNot found !\e[0m\n";
	else
		printf "\e[1;32mFound !\e[0m\n";
		SUCESS=$((SUCESS+1))
	fi
}

function	end {
	RESULT=$((100*SUCESS/TOTAL));
	printf "======================\nResults: $SUCESS/$TOTAL\n";
